#include<stdio.h>

int main()

{
    //beep sound
    printf("Beep sound\a \n");

    //backspace
    printf("back\bspace\n");

    //new line
    printf("New\nLine\n");

    //carriage return
    printf("Carriage\rReturn\n");

    //tab
    printf("Tab\tMy Name\n");

    //vertical tab
    printf("Vertical\vTab\n");

    //backslash
    printf("Back\\Slash\n");

    //quotation sign
    printf("\"Quotation sign\"\n");
}